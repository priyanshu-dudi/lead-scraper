export class UserAgentRotator {
  constructor() {
    this.index = 0;
    this.agents = USER_AGENTS;
  }

  next() {
    if (process.env.ROTATE_USER_AGENTS === 'true') {
      // Random rotation
      return this.agents[Math.floor(Math.random() * this.agents.length)];
    }
    // Sequential rotation
    const agent = this.agents[this.index % this.agents.length];
    this.index++;
    return agent;
  }

  get all() {
    return this.agents;
  }
}
